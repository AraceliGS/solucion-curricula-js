var drawMultipleTimes = function(howManyTimes, whatToDraw) {
  for(var i = 1; i <= howManyTimes; i++) {
    console.log(i + ' ' + whatToDraw);
  }
}
drawMultipleTimes(8, 'Hola');
drawMultipleTimes(5, 'Chau');
drawMultipleTimes(3, ':)');

